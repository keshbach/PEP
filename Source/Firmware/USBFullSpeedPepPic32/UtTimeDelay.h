/****************************************************************************
  Function:
    void UtTimeDelay10us(UINT32 tenMicroSecondCounter)

  Description:
    This routine performs a software delay in intervals of 10 microseconds.

  Precondition:
    None

  Parameters:
    UINT32 tenMicroSecondCounter - number of ten microsecond delays
    to perform at once.

  Returns:
    None

  Remarks:
    None
  ***************************************************************************/
void UtTimeDelay10us(UINT32 tenMicroSecondCounter);

